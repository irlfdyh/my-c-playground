#include <stdio.h>

int main() {

    int x = 5;
    int y = 8;

    printf("hasil x < y : %d \n", x<y);
    printf("hasil x > y : %d \n", x>y);
    printf("hasil x <= y: %d \n", x<=y);
    printf("hasil x >= y: %d \n", x>=y);
    printf("hasil x == y: %d \n", x==y);
    printf("hasil x != y: %d \n", x!=y);
    printf("hasil x == x: %d \n", x==x);
    printf("hasil x != x: %d \n", x!=x);

    return 0;
}